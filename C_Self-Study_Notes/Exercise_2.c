#include <stdio.h>

int main(void)
{
    int a = 1;  // 被乘數（外層迴圈控制）
    int b;      // 乘數（內層迴圈控制）

    // 外層迴圈：控制被乘數從 1 跑到 9
    while (a <= 9) {
        b = 1;  // 內層迴圈每次開始前，將乘數重設為 1

        // 內層迴圈：控制乘數從 1 跑到 9
        while (b <= 9) {
            printf("%2d x %2d = %3d\n", a, b, a * b); // 印出乘法表一行
            b++;  // 內層迴圈加 1
        }

        printf("\n"); // 每個被乘數的乘法表印完後空一行
        a++;  // 外層迴圈加 1
    }

    return 0; // 程式正常結束
}
